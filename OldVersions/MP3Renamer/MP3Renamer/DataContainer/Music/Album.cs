using System;
using System.Collections.Generic;
using MP3Renamer.DataContainer.EntityInterfaces;
using MP3Renamer.Models.DataContainer;

namespace MP3Renamer.DataContainer.Music
{
    public class Album : ISubRoot
    {
        //-----------------------------------------------------------------------------------------------------------------------
        public byte ID { get; set; }
        //-----------------------------------------------------------------------------------------------------------------------



        //-----------------------------------------------------------------------------------------------------------------------
        public Album(string PreProceedData)
        //-----------------------------------------------------------------------------------------------------------------------
        {
            this.Name = PreProceedData;
            StringManager = new RawDataManager(PreProceedData);
        }

        //-----------------------------------------------------------------------------------------------------------------------
        public IStringPartManager StringManager { get; set; }
        //-----------------------------------------------------------------------------------------------------------------------


        //-----------------------------------------------------------------------------------------------------------------------
        private List<ILeaf> SongList = null;
        public List<ILeaf> Leafs
        //-----------------------------------------------------------------------------------------------------------------------
        {
            get
            {
                if (SongList == null)
                {
                    SongList = new List<ILeaf>();
                }
                return SongList;

            }
            set
            {
                SongList = value;
            }
        }


        //-----------------------------------------------------------------------------------------------------------------------
        private short creationDate = 0;
        public short Year
        //-----------------------------------------------------------------------------------------------------------------------
        {
            get
            {
                return creationDate;
            }
            set
            {
                creationDate = value;
            }
        }

        //-----------------------------------------------------------------------------------------------------------------------
        private string AlbumName = String.Empty;
        public string Name
        //-----------------------------------------------------------------------------------------------------------------------
        {
            get
            {
                return AlbumName;
            }
            set
            {
                AlbumName = value;
            }
        }




        //-----------------------------------------------------------------------------------------------------------------------
        private byte cdcount;
        public byte CDCount
        //-----------------------------------------------------------------------------------------------------------------------
        {
            get
            {
                return cdcount;
            }
            set
            {
                cdcount = value;            
            }
        }
    }
}